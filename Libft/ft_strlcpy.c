#include "../Includes/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	length;

	length = ft_strlen((char *)src);
	i = 0;
	if (!dst || !src)
		return ((size_t) NULL);
	if (dstsize == 0)
		return (length);
	while (i < dstsize - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (length);
}
